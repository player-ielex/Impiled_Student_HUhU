package com.example.impiled_students;

public class WritingModel {

    String journ_title, journ_content, journ_photo;

    public WritingModel(){}

    public String getJourn_title() {
        return journ_title;
    }

    public void setJourn_title(String journ_title) {
        this.journ_title = journ_title;
    }

    public String getJourn_content() {
        return journ_content;
    }

    public void setJourn_content(String journ_content) {
        this.journ_content = journ_content;
    }

    public String getJourn_photo() {
        return journ_photo;
    }

    public void setJourn_photo(String journ_photo) {
        this.journ_photo = journ_photo;
    }

}
